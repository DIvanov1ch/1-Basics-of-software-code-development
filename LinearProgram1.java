public class LinearProgram1 {

    public static void main(String[] args) {

        double a;
        double b;
        double c;
        double z;

        a = 2;
        b = 5;
        c = 1;

        z = ((a - 3) * b / 2.0) + c;

        System.out.println("Значение функции z = " + z);
    }
}
